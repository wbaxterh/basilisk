// Build-time generator: reads docs/BASILISK_MVP_PLAN.md + docs/adr/*.md
// and emits a TypeScript module the whitepaper page imports. Runs as
// `prebuild` so Vercel (and local builds) inline the markdown — no
// runtime filesystem reads, no path resolution headaches.

import { promises as fs } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, '..', '..', '..');
const docsRoot = path.join(repoRoot, 'docs');
const outFile = path.resolve(__dirname, '..', 'src', 'app', 'whitepaper', 'content.generated.ts');

async function main() {
  const planBody = await fs.readFile(path.join(docsRoot, 'BASILISK_MVP_PLAN.md'), 'utf8');

  const adrDir = path.join(docsRoot, 'adr');
  const entries = await fs.readdir(adrDir);
  const adrFiles = entries.filter((f) => f.endsWith('.md') && f !== 'README.md').sort();

  const adrs = await Promise.all(
    adrFiles.map(async (f) => {
      const body = await fs.readFile(path.join(adrDir, f), 'utf8');
      const titleMatch = body.match(/^#\s+(.+)$/m);
      return {
        slug: f.replace(/\.md$/, ''),
        title: titleMatch?.[1] ?? f,
        body,
      };
    })
  );

  const plan = { slug: 'mvp-plan', title: 'MVP Implementation Plan', body: planBody };

  const out =
    '// AUTO-GENERATED by scripts/generate-whitepaper-content.mjs — do not edit.\n' +
    '// Source: docs/BASILISK_MVP_PLAN.md + docs/adr/*.md\n' +
    '\n' +
    'export interface WhitepaperDoc {\n' +
    '  slug: string;\n' +
    '  title: string;\n' +
    '  body: string;\n' +
    '}\n' +
    '\n' +
    `export const plan: WhitepaperDoc = ${JSON.stringify(plan, null, 2)} as const;\n` +
    '\n' +
    `export const adrs: WhitepaperDoc[] = ${JSON.stringify(adrs, null, 2)} as const;\n`;

  await fs.mkdir(path.dirname(outFile), { recursive: true });
  await fs.writeFile(outFile, out, 'utf8');
  console.log(`[whitepaper] wrote ${path.relative(repoRoot, outFile)} — plan + ${adrs.length} ADR(s)`);
}

main().catch((err) => {
  console.error('[whitepaper] generation failed:', err);
  process.exit(1);
});
