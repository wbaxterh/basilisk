// Build-time generator: reads docs/BASILISK_WHITEPAPER.md (the public,
// narrative whitepaper) and emits a TypeScript module the whitepaper
// page imports. The engineering MVP plan + ADRs stay in the repo as
// appendix material — they aren't inlined into the public page.
//
// Runs as `prebuild` so Vercel (and local builds) inline the markdown —
// no runtime filesystem reads, no path resolution headaches.

import { promises as fs } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, '..', '..', '..');
const docsRoot = path.join(repoRoot, 'docs');
const outFile = path.resolve(__dirname, '..', 'src', 'app', 'whitepaper', 'content.generated.ts');

async function main() {
  // Skip generation when docs/ isn't reachable (e.g., Vercel uploads only
  // apps/web). The committed content.generated.ts is used as-is in that case.
  try {
    await fs.access(docsRoot);
  } catch {
    console.log(`[whitepaper] ${path.relative(repoRoot, docsRoot)} not present — using committed content.generated.ts`);
    return;
  }

  const body = await fs.readFile(path.join(docsRoot, 'BASILISK_WHITEPAPER.md'), 'utf8');
  const plan = { slug: 'whitepaper', title: 'Basilisk Whitepaper', body };
  const adrs = []; // intentionally empty — ADRs live in the repo, not the public page

  const out =
    '// AUTO-GENERATED by scripts/generate-whitepaper-content.mjs — do not edit.\n' +
    '// Source: docs/BASILISK_WHITEPAPER.md\n' +
    '\n' +
    'export interface WhitepaperDoc {\n' +
    '  slug: string;\n' +
    '  title: string;\n' +
    '  body: string;\n' +
    '}\n' +
    '\n' +
    `export const plan: WhitepaperDoc = ${JSON.stringify(plan, null, 2)} as const;\n` +
    '\n' +
    `export const adrs: WhitepaperDoc[] = ${JSON.stringify(adrs, null, 2)} as const;\n`;

  await fs.mkdir(path.dirname(outFile), { recursive: true });
  await fs.writeFile(outFile, out, 'utf8');
  console.log(`[whitepaper] wrote ${path.relative(repoRoot, outFile)} — ${(body.length / 1024).toFixed(1)} KB`);
}

main().catch((err) => {
  console.error('[whitepaper] generation failed:', err);
  process.exit(1);
});
